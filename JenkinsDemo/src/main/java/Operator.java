
public class Operator {
	
	  public int multiply(int x, int y) {
		return x*y;
	}
	  public float AreaOfCircle(int r) {
	   return (float) (3.14*(r*r));
	}
	  public int Rectangle(int l, int b) {
	   return l*b;
    }
      public float AreaofTriangle(int b, int h) {
       return (float) (0.5*(b*h));
    }
}